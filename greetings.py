def say_hello(name):
    print("Hello " + name)
say_hello("Rick")
say_hello("Nick")
say_hello("me")

def add(number_one, number_two):
    result = number_one + number_two
    print(result)
add(3,4)

def substract(number_one, number_two):
    result = number_one - number_two
    print(result)
substract(10,5)

def multiply(number_one, number_two, number_three):
    result = number_one * number_two * number_three
    print(result)
multiply(3,2,4)

